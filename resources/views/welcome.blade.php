<script type="text/javascript">
    let arr = {
        countable: @json($countable),
        limitable: @json($limitable)
    };
    function check_datetime () {
        let store = $('#store').val();
        let date  = $('#date').val();
        $('#time option').each(function () {
            let key = [store, date, this.dataset.value].join('_');
            if(key in arr.limitable && (!arr.countable[key] || arr.limitable[key] > arr.countable[key])) {
                // 可預約
                $(this).html(this.dataset.value);
                $(this).removeAttr('disabled');
            }
            else {
                $(this).html(this.dataset.value + ' (已額滿)');
                $(this).attr('disabled','disabled');
            }
        });
    }
    $(function () {
        check_datetime();
    })
</script>
